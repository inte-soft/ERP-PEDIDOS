    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        stopEditing();
        for (int i = 0; i < jTable1.getRowCount(); i++) {
            for (EntregaDto entregaDto : consumiblesEntregaDto) {
                if (entregaDto.getId().equals(BigInteger.valueOf(Integer.parseInt( jTable1.getValueAt(i, 0).toString())))) {
                    entregaDto.setAlistado(Double.parseDouble(jTable1.getValueAt(i, 10).toString()));
                    entregaDto.setObservacion(jTable1.getValueAt(i, 12).toString());
                }
            }
        }
        pedidoConsumiblesController.updateEntregaConsumibles(consumiblesEntregaDto);
        listadoPendiente.loadConsumibles();
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

    public void stopEditing(){
        if (jTable1.isEditing()) {
            jTable1.getCellEditor().stopCellEditing();
        }
    }
    private void loadConsumibles(){
        consumiblesEntregaDto = pedidoConsumiblesController.getConsumiblesByOtAndItemUnicConsumible(ot, listItem, area, codigo);
        model.setRowCount(0);
        for(EntregaDto entregaDto : consumiblesEntregaDto){
            if (entregaDto.getObservacion() == null) {
                entregaDto.setObservacion("");
            }
            model.addRow(
                    new Object[]{
                            entregaDto.getId(),
                            entregaDto.getOt(),
                            entregaDto.getItem(),
                            entregaDto.getCodigo(),
                            entregaDto.getDescripcion(),
                            entregaDto.getTipo(),
                            entregaDto.getReferencia(),
                            entregaDto.getMarca(),
                            entregaDto.getUnidad(),
                            entregaDto.getCantidad(),
                            entregaDto.getAlistado(),
                            entregaDto.getCantidad() - entregaDto.getAlistado(),
                            entregaDto.getObservacion()
                            });
        }
    }


